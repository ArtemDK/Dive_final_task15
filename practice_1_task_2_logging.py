# ЗАДАЧА: Треугольник существует только тогда, когда сумма любых двух его сторон больше третьей.
# Дано a, b, c - стороны предполагаемого треугольника. Требуется сравнить длину каждого отрезка-стороны с суммой двух других.
# Если хотя бы в одном случае отрезок окажется больше суммы двух других, то треугольника с такими сторонами не существует.
# Отдельно сообщить является ли треугольник разносторонним, равнобедренным или равносторонним, только если треугольник существует.


import logging

# Настройка логирования с кодировкой UTF-8
logging.basicConfig(filename='triangle_log.txt',
                    level=logging.INFO,
                    format='%(asctime)s - %(levelname)s - %(message)s',
                    encoding='utf-8')


def check_triangle(a, b, c):
    if a <= 0 or b <= 0 or c <= 0:
        logging.error("Стороны треугольника должны быть положительными. Введенные значения: a=%s, b=%s, c=%s", a, b, c)
        print("Стороны треугольника должны быть положительными")
        return

    if a + b <= c or a + c <= b or b + c <= a:
        logging.info("Треугольник не существует. Введенные значения: a=%s, b=%s, c=%s", a, b, c)
        print("Треугольник не существует")
    else:
        logging.info("Треугольник существует. Введенные значения: a=%s, b=%s, c=%s", a, b, c)
        print("Треугольник существует")
        if a == b == c:
            logging.info("Тип треугольника: равносторонний.")
            print("Треугольник равносторонний")
        elif a == b or a == c or b == c:
            logging.info("Тип треугольника: равнобедренный.")
            print("Треугольник равнобедренный")
        else:
            logging.info("Тип треугольника: разносторонний.")
            print("Треугольник разносторонний")


# Пример ввода
try:
    a = float(input("Введите длину первой стороны треугольника: "))
    b = float(input("Введите длину второй стороны треугольника: "))
    c = float(input("Введите длину третьей стороны треугольника: "))

    check_triangle(a, b, c)
except ValueError:
    logging.error("Ошибка ввода: Введены некорректные значения")
    print("Ошибка ввода: Пожалуйста, введите числовые значения для длин сторон.")
